# ABOUT: None

# The data type `None` is used as the return type of functions that
# do not return anything back to the caller.

# This function below...

# def main() -> None:
#     pass


# Is the same as this:

# def main():
#     pass

# ______________________________________________________________________________

def main() -> None:
    pass

if __name__ == "__main__":
    main()

